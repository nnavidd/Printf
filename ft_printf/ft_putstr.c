#include "ft_printf.h"

int	ft_putstr(char const *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (*s != '\0')
	{
		write(1, s++, 1);
		i++;
	}
	return (i);
}
